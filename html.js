export function tag(tagName) {
    return function (properties) {
        var children = [];
        for (var _i = 1; _i < arguments.length; _i++) {
            children[_i - 1] = arguments[_i];
        }
        return Compote.apply(void 0, [tagName, properties].concat(children));
    };
}
